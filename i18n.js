window.t = function (key) {
  var lang = (window.SnapState && window.SnapState.lang) || "en";
  var dict = window.I18N[lang] || window.I18N.en;
  return dict[key] != null ? dict[key] : (window.I18N.en[key] != null ? window.I18N.en[key] : key);
};

/* Localized food name helper. */
window.foodName = function (food) {
  var lang = (window.SnapState && window.SnapState.lang) || "en";
  return lang === "ar" && food.ar ? food.ar : food.en;
};
window.servingName = function (serving) {
  var lang = (window.SnapState && window.SnapState.lang) || "en";
  return lang === "ar" && serving.ar ? serving.ar : serving.en;
};
